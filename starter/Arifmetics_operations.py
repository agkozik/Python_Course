# использование библиотеки math
import math
i = -5.148
print(i)
# отбрасывание дробной части
print('trunc(x) = ', math.trunc(i))
# возвращает ближайшее число Меньшее (floor - пол)
print('floor(i) = ', math.floor(i))
# возвращает ближайшее число Большее (ceil - потолок)
print('ceil(i) = ', math.ceil(i))

